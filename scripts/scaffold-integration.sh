- TODO: commands to reverse the setup.

## Cost expectations

- TODO: at personal scale, upstream free-tier coverage.
EOF
echo "  $DOCS_FILE"

echo ""
echo "✅ Scaffold complete for '$SNAKE' ($CAMEL)."
echo ""
cat <<EOF
Next steps:

  1. READ \`docs/integration-pattern.md\` end-to-end if you haven't.
     Start with the "Quick start" five questions — answering all
     five BEFORE coding prevents mid-implementation re-architecture.

  2. DECIDE: HMAC-signed webhook token (gcal-style) OR
     upstream-signed JWT verification (gmail-style)?
     Delete whichever you're NOT using:
       rm controller/src/$SNAKE/webhook_token.rs   # if using JWT
       rm controller/src/$SNAKE/pubsub_jwt.rs      # if using HMAC

  3. OPEN the reference implementation most similar to your upstream:
       controller/src/gmail/     (Pub/Sub + JWT + OAuth-with-history)
       controller/src/google_calendar/  (webhook + HMAC + sync-token)

  4. IMPLEMENT the ten pieces in the order listed in the pattern
     doc's "Required pieces" checklist. Compile + test green
     between each piece.

  5. WIRE UP in main.rs:
       mod $SNAKE;
       ...route + service construction...

  6. WIRE UP the frontend component in
     frontend/src/components/settings/IntegrationsManager.tsx
     (add import + render below the other integration panels).

  7. COMPLETE docs/$SNAKE-push-setup.md so the next operator can
     reproduce the setup cold.

Suggested Claude Code workflow (if that's what you're using):
  * Spawn Explore subagent × 2 in parallel against gmail/ and
    google_calendar/ for a fast survey.
  * Spawn Plan subagent to sequence the ten files.
  * Implement + test green.
  * Spawn code-review subagent on the webhook_token/pubsub_jwt
    and renewal paths specifically.
EOF
